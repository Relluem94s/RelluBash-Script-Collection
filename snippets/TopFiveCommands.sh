HISTFILE=~/.bash_history
set -o history

echo "Top 5 of your commands are:"

history |
    awk '{print $2}' | 
    awk 'BEGIN {FS="|"}{print $1}' | 
    sort | 
    uniq -c | 
    sort -n | 
    tail -5 | 
    sort -nr

